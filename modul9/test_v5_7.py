from v5_7 import daily_ohlc, daily_ohlc_optimized

def test_daily_ohlc():
    data = [
        ('2025-01-14', '10:03', 74),
        ('2025-01-14', '12:27', 94),
        ('2025-01-14', '15:42', 64),
        ('2025-01-14', '17:12', 84),
        ('2025-01-15', '09:39', 75),
        ('2025-01-15', '13:18', 85),
    ]
    expected = [
        ('2025-01-14', 74, 94, 64, 84),
        ('2025-01-15', 75, 85, 75, 85),
    ]
    assert daily_ohlc(data) == expected

